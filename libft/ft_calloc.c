#include "libft.h"
#include <stdlib.h>

void	*ft_calloc(size_t nmemb, size_t size)
{
	unsigned int	i;
	char			*ptr;

	if (nmemb == 0 || size == 0)
		return (malloc(0));
	if (nmemb > (size_t) - 1 / size)
		return (NULL);
	i = nmemb * size;
	ptr = malloc(i);
	if (ptr == NULL)
		return (NULL);
	ft_memset(ptr, 0, i);
	return (ptr);
}
